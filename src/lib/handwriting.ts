// One SVG path ("M x y L x y …") per stroke — pen-down..pen-up polylines.
export const strokePaths: string[] = tuples.map((pts) =>
	pts.map(([x, y], i) => `${i === 0 ? 'M' : 'L'}${x} ${y}`).join(' ')
);

// Tight bounding box over every point, for a fitted viewBox.
export const handwritingBox = (() => {
	let minX = Infinity,
		minY = Infinity,
		maxX = -Infinity,
		maxY = -Infinity;
	for (const pts of tuples)
		for (const [x, y] of pts) {
			if (x < minX) minX = x;
			if (y < minY) minY = y;
			if (x > maxX) maxX = x;
			if (y > maxY) maxY = y;
		}
	const pad = 8;
	return {
		x: minX - pad,
		y: minY - pad,
		width: maxX - minX + pad * 2,
		height: maxY - minY + pad * 2
	};
})();
